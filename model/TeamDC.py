class TeamDC:
    name=""
    height=0.0
    weight=0.0
    games_played=0

    def set_name(self,player_name):
        self.name = player_name
    def get_name(self):
        return self.name

    def set_height(self,player_height):
        self.height = player_height
    def get_height(self):
        return self.height

    def set_weight(self,player_weight):
        self.weight = player_weight
    def get_weight(self):
        return self.weight

    def set_games_played(self,player_games_played):
        self.games_played = player_games_played
    def get_games_played(self):
        return self.games_played